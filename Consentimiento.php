		<h4>Justificación y objetivo del estudio:</h4>
		<p>El agotamiento es el grado de fatiga y agotamiento físico y psicológico que experimenta la persona, en relación con su trabajo y en relación con su trabajo con pacientes. Aunque existen múltiples estudios que usan el Inventario de Agotamiento de Maslach para su evaluación, se sabe que dicho instrumento tiene múltiples deficiencias que motivan el uso de otro instrumento, como el Inventario de Agotamiento de Copenhague. La realización de este estudio permitiría determinar si este último es útil en población mexicana. El objetivo de esta investigación es evaluar la validez y confiabilidad del Inventario de Agotamiento de Copenhague en médicos residentes mexicanos, así como determinar la prevalencia y factores de riesgo de agotamiento entre médicos residentes mexicanos.</p>
